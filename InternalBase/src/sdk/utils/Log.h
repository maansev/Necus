#pragma once

// =============================================================================
//  Log.h — crash-diagnostics logger
//  Пишет в C:\necus_log.txt (fallback: %TEMP%\necus_log.txt) с немедленным
//  flush, чтобы последняя строка перед крашем гарантированно попала в файл.
//  Также ставит Vectored Exception Handler, который логирует адрес/код
//  исключения и имя модуля до того, как процесс умрёт.
// =============================================================================

namespace Log {
    void Init();                       // открыть файл + установить VEH
    void Write(const char* fmt, ...);  // printf-style, авто-\n, мгновенный flush
    void Shutdown();

    // Дешёвая метка "где мы сейчас" — просто записывает указатель на строковый
    // литерал (без I/O). VEH при краше логирует последний чекпоинт.
    void Checkpoint(const char* name);
}
